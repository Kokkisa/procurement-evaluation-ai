"""Final PDF generation.

Block 7 ships a *placeholder* implementation so the /approve endpoint can
return a real file path. Block 9 will replace the body of
``generate_final_pdf`` with the formal ReportLab-based PSU technical
evaluation report (header, matrix table, signature blocks, audit-log
appendix). The function signature is intentionally kept stable so /approve
doesn't need to change in Block 9.
"""

from __future__ import annotations

from pathlib import Path
from uuid import UUID

from reportlab.lib.pagesizes import A4
from reportlab.pdfgen import canvas


def generate_final_pdf(
    eval_id: UUID,
    tender_number: str,
    output_dir: Path,
) -> Path:
    """Write a placeholder PDF for ``eval_id`` and return its path.

    Block 9 replaces the body but keeps this signature.
    """
    output_dir.mkdir(parents=True, exist_ok=True)
    safe = tender_number.replace("/", "_").replace("\\", "_")
    out = output_dir / f"{safe}_technical_evaluation.pdf"

    c = canvas.Canvas(str(out), pagesize=A4)
    c.setTitle(f"Technical Evaluation - {tender_number}")
    c.drawString(72, 800, "TECHNICAL EVALUATION (PLACEHOLDER)")
    c.drawString(72, 770, f"Tender: {tender_number}")
    c.drawString(72, 750, f"Evaluation ID: {eval_id}")
    c.drawString(
        72,
        720,
        "This placeholder is generated by the Block 7 stub.",
    )
    c.drawString(
        72,
        700,
        "Block 9 will replace this with the formal evaluation PDF",
    )
    c.drawString(72, 680, "(header, criterion x vendor matrix, signature blocks, audit appendix).")
    c.save()
    return out
